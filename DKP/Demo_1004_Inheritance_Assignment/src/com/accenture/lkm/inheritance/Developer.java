package com.accenture.lkm.inheritance;

public class Developer extends Employee{
	
	private String projectName;
	private String projectDuratoin;
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getProjectDuratoin() {
		return projectDuratoin;
	}
	public void setProjectDuratoin(String projectDuratoin) {
		this.projectDuratoin = projectDuratoin;
	}
	
	

}
